# creating list:
my_list = [10, 20, 30, 40]
print(my_list)
# Accessing elements:
print(my_list[0])
print(my_list[:3])
# Modifying elements:
my_list[0] = 10
print(my_list)
# Adding elements:
my_list.append(50)
print(my_list)
my_list.insert(1, 5)
print(my_list)
# Removing elements:
my_list.pop()
print(my_list)
my_list.remove(20)
print(my_list)